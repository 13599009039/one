    /**
     * 获取汉字的拼音首字母
     * @param {string} char - 单个汉字
     * @returns {string} - 拼音首字母（小写）
     */
    function getCharPinyin(char) {
        if (!char || char.length !== 1) return '';
        
        // 直接查表
        if (pinyinMap[char]) {
            return pinyinMap[char];
        }
        
        // 如果是英文字母，直接返回小写
        if (/[a-zA-Z]/.test(char)) {
            return char.toLowerCase();
        }
        
        // 如果是数字，直接返回
        if (/[0-9]/.test(char)) {
            return char;
        }
        
        // 其他字符返回空
        return '';
    }
    
    /**
     * 获取字符串的拼音首字母序列
     * @param {string} str - 输入字符串
     * @returns {string} - 拼音首字母序列
     */
    function getPinyinInitials(str) {
        if (!str) return '';
        
        let result = '';
        for (let i = 0; i < str.length; i++) {
            result += getCharPinyin(str[i]);
        }
        return result;
    }
    
    /**
     * 模糊搜索匹配
     * 支持：汉字直接匹配、拼音首字母匹配、混合匹配
     * @param {string} text - 被搜索的文本
     * @param {string} keyword - 搜索关键词
     * @returns {boolean} - 是否匹配
     */
    function fuzzyMatch(text, keyword) {
        if (!text || !keyword) return !keyword; // 空关键词匹配所有
        
        text = text.toLowerCase();
        keyword = keyword.toLowerCase();
        
        // 1. 直接包含匹配
        if (text.includes(keyword)) {
            return true;
        }
        
        // 2. 拼音首字母匹配
        const textPinyin = getPinyinInitials(text);
        if (textPinyin.includes(keyword)) {
            return true;
        }
        
        // 3. 拼音首字母前缀匹配（从任意位置开始）
        for (let i = 0; i <= textPinyin.length - keyword.length; i++) {
            if (textPinyin.substring(i).startsWith(keyword)) {
                return true;
            }
        }
        
        // 4. 每个字的拼音首字母连续匹配
        let keyIndex = 0;
        for (let i = 0; i < textPinyin.length && keyIndex < keyword.length; i++) {
            if (textPinyin[i] === keyword[keyIndex]) {
                keyIndex++;
            }
        }
        if (keyIndex === keyword.length) {
            return true;
        }
        
        return false;
    }
    
    /**
     * 在数组中搜索匹配项
     * @param {Array} items - 数据数组
     * @param {string} keyword - 搜索关键词
     * @param {string|Function} textField - 文本字段名或获取文本的函数
     * @returns {Array} - 匹配的项
     */
    function searchInArray(items, keyword, textField) {
        if (!items || !Array.isArray(items)) return [];
        if (!keyword) return items;
        
        return items.filter(item => {
            let text;
            if (typeof textField === 'function') {
                text = textField(item);
            } else if (typeof textField === 'string') {
                text = item[textField];
            } else {
                text = String(item);
            }
            return fuzzyMatch(text, keyword);
        });
    }
    
